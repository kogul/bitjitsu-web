    <p>
        The organisers host (usually) a strategy game on the internet and build a platform to allow users to submit AI scripts that are programmed to play this game. The organisers release an API that describes how the AI-bot should communicate with the game-server.
        Tournaments are held between bots that comprise of numerous game-sessions, or rounds. For each game, every participating bot is awarded a score based on it's game playing abilities. The rating is used to setup a leaderboard where everyone can see the rankings.
